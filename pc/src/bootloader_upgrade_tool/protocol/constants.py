    RAM_LOAD_DATA = 0x0102
    RAM_LOAD_END = 0x0103
    ERASE = 0x0201
    PROGRAM_BEGIN = 0x0210
    PROGRAM_DATA = 0x0211
    PROGRAM_END = 0x0212
